    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        // TODO add your handling code here:
        Object[] options = {"Si","No"};
        int confirm = JOptionPane.showOptionDialog(
                null,
                "¬øEstas seguro que deseas eliminar este libro?",
                "Confirmar eliminaci√≥n",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                options,
                options[1]
        );
        if(confirm == JOptionPane.YES_OPTION){
            File image = new File(String.valueOf(lblImage.getIcon()));
            if(image.exists()){
                image.delete();
            }
            DAOBooks dao = new DAOBooksImpl();
            int bookId = Integer.parseInt(lblId.getText());
            try {
                dao.eliminar(bookId);
                this.getParent().remove(this);
                Dashboard.ShowJPanel(new com.mycompany.views.Books());
            } catch (Exception ex) {
                Logger.getLogger(BookInfo.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnDeleteMouseClicked

